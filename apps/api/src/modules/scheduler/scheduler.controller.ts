import { Body, Controller, Post, HttpCode, HttpStatus, UseGuards } from '@nestjs/common';
import { ApiTags, ApiOperation, ApiSecurity } from '@nestjs/swagger';
import { SkipThrottle } from '@nestjs/throttler';
import { SchedulerService } from './scheduler.service';
import { ServiceApiKeyGuard } from '../../common/guards/service-api-key.guard';
import { Public } from '../../common/decorators';

@ApiTags('Scheduler')
@ApiSecurity('scheduler-key')
@Public()
@SkipThrottle()
@UseGuards(ServiceApiKeyGuard)
@Controller('scheduler')
export class SchedulerController {
  constructor(private readonly schedulerService: SchedulerService) {}

  @Post('billing')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({ summary: 'Trigger monthly billing (external cron)' })
  async runBilling() {
    return this.schedulerService.runBilling();
  }

  @Post('expenses')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({ summary: 'Trigger monthly expenses generation (external cron)' })
  async runExpenses() {
    return this.schedulerService.runExpenses();
  }

  @Post('overdue-invoices')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({ summary: 'Mark overdue invoices (external cron)' })
  async markOverdueInvoices() {
    return this.schedulerService.markOverdueInvoices();
  }

  @Post('invoice-card-reminders')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Create due Invoice Card reminder notification jobs (external cron)',
    description:
      'Creates idempotent NotificationEvent/NotificationJob records from Invoice Card rules. It does not send external client messages.',
  })
  async runInvoiceCardReminders() {
    return this.schedulerService.runInvoiceCardReminders();
  }

  @Post('expense-backlog-reminders')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Expense Backlog reminder jobs (external cron)',
    description:
      'Creates idempotent NotificationEvent/NotificationJob records: one weekly digest per UTC week for open backlog cards (BACKLOG + backlog reason + unpaid balance), and per-day reminders for backlog cards whose due date has passed. Does not send external messages.',
  })
  async runExpenseBacklogReminders() {
    return this.schedulerService.runExpenseBacklogReminders();
  }

  @Post('expense-plan-auto-due')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Run NBOS expense plan auto-generate for due dates (external cron)',
    description:
      'Delegates to ExpensePlansService.autoGenerateDuePlans (same as POST /expense-plans/actions/auto-generate-due). Optional in-process cron when SCHEDULER_EXPENSE_PLAN_AUTO_DUE_ENABLED=true (see env example).',
  })
  async runExpensePlanAutoDue() {
    return this.schedulerService.runExpensePlanAutoDue();
  }

  @Post('report-schedules-due')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Run due scheduled report exports (external cron)',
    description:
      'Creates queued ReportExportJob records for active ReportSchedule rows whose nextRunAt is due. Optional in-process cron when REPORT_SCHEDULES_DUE_CRON_ENABLED=true.',
  })
  async runReportSchedulesDue() {
    return this.schedulerService.runReportSchedulesDue();
  }

  @Post('sales-kpi-month-close')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Finalize Sales KPI snapshots for earned month (external cron)',
    description:
      'Syncs KpiResult rows from sales payment facts for all employees with an active KPI policy. Defaults to prior calendar month vs UTC today. Optional body `{ "earnedPeriod": "YYYY-MM" }`.',
  })
  async runSalesKpiMonthClose(@Body() body: { earnedPeriod?: string }) {
    return this.schedulerService.runSalesKpiMonthClose(body);
  }

  @Post('sales-kpi-backfill-all')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Repair Sales KPI + payable snapshots for every earned month with Sales bonuses',
    description:
      'Runs month-close sync + open-bonus payable refresh for each distinct `earnedPeriod` on Sales bonus entries. Use after migrations or data fixes.',
  })
  async runSalesKpiBackfillAll() {
    return this.schedulerService.runSalesKpiBackfillAll();
  }

  @Post('credential-trash-purge')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Purge trashed credentials past retention TTL (external cron)',
    description:
      'Hard-deletes credentials in Trash longer than CREDENTIAL_TRASH_RETENTION_MS (default 30 days). Optional in-process cron when SCHEDULER_CREDENTIAL_TRASH_PURGE_ENABLED=true.',
  })
  async runCredentialTrashPurge() {
    return this.schedulerService.runCredentialTrashPurge();
  }

  @Post('support-sla-escalation')
  @HttpCode(HttpStatus.OK)
  @ApiOperation({
    summary: 'Support SLA warning / breach in-app notifications (external cron)',
    description:
      'Scans open tickets; creates idempotent in-app notifications for resolve warning, response breach, and resolve breach. Waiting overlay pauses the SLA clock.',
  })
  async runSupportSlaEscalation() {
    return this.schedulerService.runSupportSlaEscalation();
  }
}
